/**
 * Inject auth toast translations into i18n resource bundles.
 * Call after i18n initialization and whenever a new language is loaded.
 */
export function injectAuthToastTranslations(lang?: string): void {
  const targetLangs = lang ? [lang] : Object.keys(authToastTranslations);
  
  for (const l of targetLangs) {
    const translations = authToastTranslations[l];
    if (!translations) continue;
    
    i18n.addResourceBundle(l, 'translation', { toasts: translations }, true, true);
  }
}

// Auto-inject on import for all pre-defined languages
