export function injectCommunityTranslations(i18nInstance: I18nInstance) {
  Object.entries(communityTranslations).forEach(([lang, translations]) => {
    if (i18nInstance.hasResourceBundle(lang, 'translation')) {
      i18nInstance.addResourceBundle(lang, 'translation', translations, true, true);
    } else {
      // Store for later — when the locale is lazily loaded, merge then
      const origAddBundle = i18nInstance.addResourceBundle.bind(i18nInstance);
      i18nInstance.on('languageChanged', (lng) => {
        if (lng === lang && communityTranslations[lng]) {
          origAddBundle(lng, 'translation', communityTranslations[lng], true, true);
        }
      });
      // Also inject now in case the bundle exists but we missed it
      i18nInstance.addResourceBundle(lang, 'translation', translations, true, true);
    }
  });
}
